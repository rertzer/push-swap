#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*last;

	if (lst == NULL)
		return (NULL);
	last = lst;
	while (1)
	{
		if (last->next)
			last = last->next;
		else
			break ;
	}
	return (last);
}
