#include "push_swap.h"

void	ps_sort_array(int *tab, int size)
{
	int	j;

	while (size > 0)
	{
		size--;
		j = 0;
		while (j < size)
		{
			if (tab[j + 1] < tab[j])
				swap(&tab[j], &tab[j + 1]);
			j++;
		}
	}
}
